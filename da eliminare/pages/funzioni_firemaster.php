    <h2 style="margin-bottom: 20px;">Funzionalità del sito Fire Master</h2>

    <p><strong>🧭 Bussola e orientamento del dispositivo</strong><br>
    Visualizza in tempo reale l’orientamento del dispositivo rispetto al Nord, mostrando una rosa dei venti dinamica e le informazioni GPS: azimut, coordinate, quota, comune e provincia.</p>

    <p><strong>💧 Ricerca delle vasche di approvvigionamento idrico</strong><br>
    Calcola i 10 punti acqua più vicini, il tempo di rotazione medio basato su 70 miglia nautiche orarie e stima il numero massimo di lanci in 130 minuti. Attenzione: non tutte le vasche sono verificate in tempo reale.</p>

    <p><strong>🔥 Mappa per calcolo area e perimetro incendio</strong><br>
    Permette al DOS di disegnare il perimetro di un incendio sulla mappa, calcolare l’area e il perimetro e scaricare il poligono in formato KML per l’analisi desktop successiva.</p>

    <p><strong>🌙 Calcolo delle effemeridi per un comune calabrese</strong><br>
    Genera un calendario per un mese e un anno selezionati, visualizzando alba, tramonto, festività e orario solare o legale per ciascun giorno del mese, in base al comune selezionato.</p>

    <p><strong>📷 Scatto fotografico con logo e metadati</strong><br>
    Permette di scattare una foto con la fotocamera del dispositivo, aggiungendo logo aziendale, coordinate, quota, nome e sigla del DOS e una nota personalizzabile sovrimpresse nell'immagine.</p>

    <p><strong>🧭 Conversione coordinate geografiche DD ⇄ DMS</strong><br>
    Converte con precisione le coordinate tra formato decimale (DD) e gradi, minuti e secondi (DMS), utili in campo o nei documenti tecnici.</p>

    <p><strong>📏 Conversione di misure utili al DOS</strong><br>
    Consente la conversione di distanze (metri, km, miglia nautiche) e superfici (ettari, m², km²), fondamentali durante la pianificazione e l’intervento.</p>
